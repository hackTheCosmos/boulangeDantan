                    <p>La cuisson du pain se faisait généralement dans un four à bois, qui était chauffé à une température élevée. Avant d'enfourner le pain, le four était souvent préchauffé pendant un certain temps pour créer une chaleur uniforme. Le pain était alors placé directement sur la sole du four ou sur des plaques spéciales et cuit jusqu'à ce qu'il soit doré et croustillant à l'extérieur, avec une mie tendre à l'intérieur.</p>
