                <h3>Façonnage</h3>
                <div class="slideContent__wrapper">
                    <img src="Public/medias/images/diapo/faconnage.jpg" alt="petrissage de la pâte à pain">
                    <p>Une fois que la pâte avait reposé suffisamment longtemps, elle était façonnée en différentes formes selon les besoins. Les techniques de façonnage variaient en fonction du type de pain que l'on voulait obtenir, que ce soit des boules, des baguettes, des pains plats, ou d'autres formes traditionnelles. Le façonnage demandait souvent de la pratique pour obtenir des formes régulières et des textures optimales.</p>
