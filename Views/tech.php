        <h2>Faire et entretenir son levain</h2>
        <div class="bottom__border"></div>
            <div class="item__wrapper">    
                <p>Pour faire et entretenir son levain, commencez par mélanger 50 g de farine de seigle ou de blé complet avec 50 ml d'eau tiède dans un bocal propre. Couvrez le mélange d'un linge et laissez-le reposer à température ambiante pendant 24 heures. Chaque jour, pendant environ une semaine, ajoutez 50 g de farine et 50 ml d'eau, en mélangeant bien et en laissant reposer à nouveau. Le levain commencera à bouillonner et à développer une odeur légèrement aigre, signe que les bactéries et les levures naturelles sont actives.</p>

                <img src="Public/medias/images/tech/levain.jpg" alt="image levain">        
            </div>
            <p>Pour entretenir votre levain, conservez-le au réfrigérateur et rafraîchissez-le une à deux fois par semaine en en retirant une partie (environ la moitié) et en ajoutant à nouveau 50 g de farine et 50 ml d'eau. Avant de l'utiliser dans vos recettes, sortez-le du réfrigérateur et nourrissez-le quelques heures auparavant pour qu'il retrouve toute son activité.</p>
