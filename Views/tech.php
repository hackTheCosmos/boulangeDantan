                <h3>Repos de la pâte</h3>
                <div class="slideContent__wrapper">
                    <img src="Public/medias/images/diapo/repos.jpeg" alt="repos de la pâte à pain">
                    <p>Après la préparation de la pâte, celle-ci était laissée à reposer dans un endroit chaud et humide pour permettre la fermentation. Ce repos était crucial pour développer la saveur du pain et rendre la pâte plus facile à manipuler lors du façonnage. La durée du repos variait selon la recette et les conditions environnantes, mais cela pouvait prendre plusieurs heures, voire toute une nuit.</p>
                </div>
            </div>
