                <h3>Préparation de la pâte</h3>
                <div class="slideContent__wrapper">
                    <img src="Public/medias/images/diapo/petrissage.jpg" alt="petrissage de la pâte à pain">
                    <p>Dans les temps anciens, la préparation de la pâte se faisait souvent à la main, en mélangeant simplement de la farine avec de l'eau pour former une pâte grossière. Parfois, des ingrédients supplémentaires comme le sel ou le levain naturel étaient ajoutés à ce stade. La pâte était pétrie énergiquement jusqu'à ce qu'elle soit lisse et élastique.</p>
                </div>
