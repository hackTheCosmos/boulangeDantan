        <div class="bottom__border"></div>
        <div class="item__wrapper">
            <p>La culture responsable est au cœur du métier de paysan boulanger. Il s'engage à cultiver nos terres de manière écologique, en favorisant des méthodes durables et respectueuses de la biodiversité. De la sélection des semences à la rotation des cultures, il veille à préserver la fertilité des sols et à minimiser son empreinte environnementale.</p>
